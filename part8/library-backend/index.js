const typeDefs = `
  type Author {
    id: ID!
    name: String!
    born: String
    bookCount: Int!
  }

  type Book {
    id: ID!
    title: String!
    author: String!
    published: Int!
    genres: [String!]!
  }

  type User {
    username: String!
    favoriteGenre: String!
    id: ID!
  }

  type Token {
    value: String!
  }

  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
    me: User
  }
  
  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String!]!
    ): Book
    editAuthor(
      name: String!
      setBornTo: Int!
    ): Author
    createUser(
      username: String!
      favoriteGenre: String!
    ): User
    login(
      username: String!
      password: String!
    ): Token
  }
`

const resolvers = {
  Query: {
    bookCount: async () => Author.collection.countDocuments(),
    authorCount: async () => Book.collection.countDocuments(),
    allBooks: async (root, args) => {
      return Book.find(args.author ?
        args.genre ?
          { author: args.author, genre: args.genre } :
          { author: args.author }
        : args.genre ? { genres: args.genre } : {});
    },
    allAuthors: async () => Author.find({}),
    me: (root, args, context) => {
      return context.currentUser
    }
  },
  Author: {
    bookCount: async (root) => {
      return Book.where({ author: root.id }).countDocuments();
    }
  },
  Mutation: {
    addBook: async (root, args, context) => {
      const { author, ...rest } = args;
      const currentUser = context.currentUser

      if (!currentUser) {
        throw new GraphQLError('not authenticated', {
          extensions: {
            code: 'BAD_USER_INPUT',
          }
        })
      }

      let dbAuthor = await Author.findOne({ name: author })
      if (rest.title.length < 4) {
        return new GraphQLError('too short, title must be more than 3 chars',
          {
            extensions: {
              code: 'BAD_USER_INPUT',
              invalidArgs: args.title
            }
          })
      }
      if (!dbAuthor) {
        if (author.length < 4) {
          return new GraphQLError('too short, author name must be more than 3 chars',
            {
              extensions: {
                code: 'BAD_USER_INPUT',
                invalidArgs: author
              }
            })
        }
        author = await Author.create({ name: author, bookCount: 1 })
      }
      console.log(dbAuthor._id.toString());
      return Book.create({ author: dbAuthor._id.toString(), ...rest })
    },
    editAuthor: async (root, args, context) => {
      const author = await Author.findOne({ name: args.name })
      const currentUser = context.currentUser

      if (!currentUser) {
        throw new GraphQLError('not authenticated', {
          extensions: {
            code: 'BAD_USER_INPUT',
          }
        })
      }

      author.born = args.setBornTo;
      return await author.save();
    },
    createUser: async (root, args) => {
      console.log(args);
      return User.create({ ...args }).catch(error => {
        throw new GraphQLError('Creating the user failed ', {
          extensions: {
            code: 'BAD_USER_INPUT',
            invalidArgs: { username: args.username, favoriteGenre: args.favoriteGenre },
            error
          }
        })
      })
    },
    login: async (root, args) => {
      const user = await User.findOne({ username: args.username })

      if (!user || args.password !== 'secret') {
        throw new GraphQLError('wrong credentials: ', {
          extensions: {
            code: 'BAD_USER_INPUT'
          }
        })
      }

      const userForToken = {
        username: user.username,
        id: user._id,
      }

      return { value: jwt.sign(userForToken, process.env.JWT_SECRET) }
    }
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

startStandaloneServer(server, {
  listen: { port: 4000 },
  context: async ({ req, res }) => {
    const auth = req ? req.headers.authorization : null
    if (auth && auth.startsWith('Bearer ')) {
      const decodedToken = jwt.verify(
        auth.substring(7), process.env.JWT_SECRET
      )
      const currentUser = await User.findById(decodedToken.id)
      return { currentUser }
    }
  }
}).then(({ url }) => {
  console.log(`Server ready at ${url}`)
})